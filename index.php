      <h4 class="price">4 serving</h4>

    <p>
      (TOTAL COOK TIME 2 HOURS)
    </p>
    <h4>Ingredients</h4>
    <p>THE MEAT</p>


    <li>
      4 boneless pork loin chops (1½-ins, 3.8-cms thick)
    </li>
    <p>
      THE BRINE
    </p>
    <li>
      Water – 8 cups
    </li>
    <li>
      Red pepper, crushed – 1½ teaspoons
    </li>
    <li>
      Salt – 1½ teaspoons
    </li>
    <p>
      THE RUB
    </p>
    <li>
      Unsweetened cocoa powder – 1 tablespoon
    </li>
  <li>
    Brown sugar – 1 tablespoon
  </li>
<li>
  Ancho chile powder – 1 tablespoon or 1 teaspoon
</li>
<li>
regular chili powder and 1/8-1/4 teaspoon crushed red pepper
</li>
<li>
Salt – 1 tablespoon
</li>
<li>
Olive oil
</li>
<p>
THE SMOKE
Preheat your charcoal grill to moderately high heat and create a
cool zone by raking the coals to one side
</p>
<h4>METHOD</h4>
<p>
1. First, prepare the brine. Combine the water, red pepper, and salt in
a large bowl. Stir well until the salt dissolves. Add the meat to the
brine and set aside for an hour to soak.
</p>
<p>
2. In the meantime, prepare the rub. Combine the cocoa powder,
brown sugar, chili powder, and salt on a large plate.
</p>
<p>
3. Remove the pork chops from the brine and pat dry using kitchen
paper. Brush each chop on both sides with olive oil then roll in the
rub mixture to coat evenly.
</p>
<p>
4. Place the chops on the grill over direct heat and cook for 4
minutes, flipping halfway through cooking. Transfer the chops to
the prepared cool zone and cover. Cook for another 15-20 minutes.
The meet should register an internal temperature of 140°F (60°C).
</p>
<p>
5. Take the meat off the grill and allow to sit for several minutes
before serving.
</p>
